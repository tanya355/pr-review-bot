import { Octokit } from "@octokit/rest";
import { OpenAIReviewer } from "./openai";
import * as fs from "fs";
import * as yaml from "js-yaml";

const octokit = new Octokit({ auth: process.env.GITHUB_TOKEN });
const reviewer = new OpenAIReviewer();

const [owner, repo] = (process.env.REPO || "").split("/");
const prNumber = parseInt(process.env.PR_NUMBER || "0");

async function main() {
  console.log(`Processing PR #${prNumber} in ${owner}/${repo}`);

  // 1. Fetch PR diff
  const { data: files } = await octokit.pulls.listFiles({
    owner,
    repo,
    pull_number: prNumber,
  });

  console.log(`Found ${files.length} files, filtering...`);
  const diff = files
    .filter((f) => !shouldIgnore(f.filename))
    .map((f) => `### ${f.filename}\n\`\`\`\n${f.patch || ""}\n\`\`\``)
    .join("\n\n");

  // 2. Load review config
  const config = yaml.load(
    fs.readFileSync("review-config.yml", "utf8")
  ) as any;

  const criteria = config.review.focus_areas;

  // 3. Call OpenAI (or Ollama if OPENAI_API_BASE is set)
  console.log("Running AI review...");
  const response = await reviewer.review({
    diff,
    criteria,
  });
  console.log(`Review completed using model: ${response.model}`);
  console.log(
    `Tokens used: ${response.usage.promptTokens} prompt, ${response.usage.completionTokens} completion`
  );

  // 4. Post comment on PR
  await octokit.issues.createComment({
    owner,
    repo,
    issue_number: prNumber,
    body: `${response.content}\n\n---\n*Generated by AI PR Review Assistant*`,
  });

  console.log("Review posted successfully.");
}

function shouldIgnore(filename: string): boolean {
  const ignorePatterns = ["*.lock", "dist/", "*.generated.ts"];
  return ignorePatterns.some((p) => filename.includes(p.replace("*", "")));
}

main().catch(console.error);
